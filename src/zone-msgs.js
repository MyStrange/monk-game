// ── Rotation counters ──────────────────────────────────────────────────────
const _counters = {};
function _pick(arr) {
  if (!arr || arr.length === 0) return null;
  // Последний элемент не цикличен — останавливается на нём
  const key = arr.join('|');
  if (!(_counters[key] >= 0)) _counters[key] = 0;
  const idx = Math.min(_counters[key], arr.length - 1);
  _counters[key]++;
  return arr[idx];
}

// ── Global fallback (любой предмет × любая зона без записи) ───────────────
const _FALLBACK = [
  'Ничего не происходит. Предмет делает вид, что не слышит.',
  'Снова ничего. Либо ты что-то делаешь не так, либо — всё так, и в этом проблема.',
  'В третий раз это уже традиция.',
];
const _fallbackCounts = {};

function _fallbackPick(key) {
  _fallbackCounts[key] = (_fallbackCounts[key] ?? 0);
  const msg = _FALLBACK[Math.min(_fallbackCounts[key], _FALLBACK.length - 1)];
  _fallbackCounts[key]++;
  return msg;
}

// ── getZoneMsg ─────────────────────────────────────────────────────────────
// itemId: string | null (null = кнопка без предмета)
// zone:   string
// item:   объект предмета (для проверки glowing, hasWater и т.д.)
export function getZoneMsg(itemId, zone, item = null) {
  if (!itemId) {
    // Bare-zone fallback (только rock)
    if (zone === 'rock1' || zone === 'rock2' || zone === 'rock3') {
      return _pick(ZONE_MSGS['rock:_']);
    }
    return null;
  }

  // Glowing jar overrides
  if ((itemId === 'jar' || itemId === 'jar_open') && item?.glowing) {
    const glowKey = `jar_glowing:${zone}`;
    if (ZONE_MSGS[glowKey]) return _pick(ZONE_MSGS[glowKey]);
    return 'Свет из банки. Тихо.';
  }

  // Exact key
  const key = `${itemId}:${zone}`;
  if (ZONE_MSGS[key]) return _pick(ZONE_MSGS[key]);

  // Rock fallback для stick и jar
  if (zone === 'rock1' || zone === 'rock2' || zone === 'rock3') {
    const rockKey = `${itemId}:rock`;
    if (ZONE_MSGS[rockKey]) return _pick(ZONE_MSGS[rockKey]);
  }

  // Глобальный fallback — ироничный текст
  return _fallbackPick(`${itemId}:${zone}`);
}

// ── resetCounters (для тестов) ─────────────────────────────────────────────
export function resetZoneMsgCounters() {
  for (const k in _counters) delete _counters[k];
}
